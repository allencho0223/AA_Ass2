
public class Persona {
    
    private static String name, hairLength, glasses, facialHair, eyeColor, pimples, hat, hairColor, noseShape, faceShape;
    
    

    public Persona(String name, String hairLength, String glasses, String facialHair, String eyeColor, String pimples, String hat, String hairColor, String noseShape, String faceShape) {
        this.name = name;
        this.hairLength = hairLength;
        this.glasses = glasses;
        this.facialHair = facialHair;
        this.eyeColor = eyeColor;
        this.pimples = pimples;
        this.hat = hat;
        this.hairColor = hairColor;
        this.noseShape = noseShape;
        this.faceShape = faceShape;
    }
}
